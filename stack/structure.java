// Linear and Non-Linear Data Structure

// Data structures are mainly divided into two types:

// Linear Data Structure
// Non-Linear Data Structure
// 1. Linear Data Structure

// A data structure where elements are arranged sequentially (one after
// another).

// Each element is connected to:

// previous element
// next element
// Structure
// 10 → 20 → 30 → 40

// Elements are stored in a single line.

// Characteristics of Linear Data Structure
// Easy to implement
// Data stored sequentially
// Traversal is simple
// Uses less complexity
// Types of Linear Data Structure
// Data Structure Description
// Array Fixed size collection
// Linked List Nodes connected with pointers
// Stack LIFO
// Queue FIFO
// Examples
// Array Example
// int arr[] = {10, 20, 30, 40};
// Stack Example
// Stack<Integer> st = new Stack<>();

// st.push(10);
// st.push(20);
// Queue Example
// Queue<Integer> q = new LinkedList<>();

// q.add(10);
// q.add(20);
// Advantages
// Simple implementation
// Easy traversal
// Memory efficient in some cases
// Disadvantages
// Limited flexibility
// Searching can be slower
// Sequential processing
// 2. Non-Linear Data Structure

// A data structure where elements are not arranged sequentially.

// One element can connect to multiple elements.

// Structure
// A
// / \
// B C
// / \ / \
// D E F G

// This is not in one straight line.

// Characteristics of Non-Linear Data Structure
// Complex structure
// Multiple relationships
// Faster searching in some cases
// Hierarchical representation
// Types of Non-Linear Data Structure
// Data Structure Description
// Tree Hierarchical structure
// Graph Network structure
// Tree Example
// 10
// / \
// 20 30
// Graph Example
// A ---- B
// | |
// C ---- D
// Advantages
// Efficient searching
// Better hierarchical storage
// Flexible relationships
// Disadvantages
// Complex implementation
// More memory usage
// Harder traversal
// Difference Between Linear and Non-Linear Data Structure
// Feature Linear DS Non-Linear DS
// Arrangement Sequential Non-sequential
// Relationship One-to-one One-to-many
// Traversal Simple Complex
// Memory Less More
// Implementation Easy Difficult
// Example Array, Stack Tree, Graph
// Real Life Examples
// Linear Non-Linear
// Queue at ticket counter Family tree
// Train compartments Organization hierarchy
// Stack of books Google Maps network
