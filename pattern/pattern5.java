public class pattern5 {
    public static void main(String[] args) {
// * * * * *
// * * * * 
// * * * 
// * *  
// * 
// step 1 = loop 1 to n i--;
// step 1 = loop 2 j<=i;
// print (*);
// step 1  when start loop  n to n i-- 
// step 2  when start lopp j =i j>=1; j--
// step 3  when start  and print evry point *
// step 4 and next line to space and print second line 
int n =5;
for(int i=n;i>=1;i--){
    for(int j=i;j>=1;j--){
        System.out.print(" *");
    }
    System.out.println();
    }

   }
   
}
